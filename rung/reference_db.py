# A re-scrape that collapses to a small fraction of the stored snapshot is almost always a
# throttled PARTIAL fetch — the aggregator 406 soft-block truncates a menu mid-pagination and
# tends to drive a store to near-zero
# — not a genuine menu shrink. Keep the larger prior rather than overwrite it with a fragment,
# the way Stage-2 stores have keep-the-best. The guard only holds while the prior is still FRESH:
# a real shrink (or a store that truly lost most of its menu) still lands once the old snapshot
# ages past the window, so a store can never be wedged on stale data forever.
_MENU_RETAIN_FRACTION = 0.5
_MENU_RETAIN_MAX_AGE_HOURS = 48.0


def _snapshot_age_hours(conn: DBConn, store_key: str) -> float | None:
    """Hours since this store's current snapshot was taken, or None if it has none."""
    # Cast to timestamptz so the max is chronological, not a lexical string max — and so psycopg
    # returns a tz-aware datetime, removing the naive-timestamp parse this used to guard against.
    row = conn.execute(
        "SELECT max(scraped_at::timestamptz) FROM store_products WHERE store_key = %s",
        (store_key,),
    ).fetchone()
    if row is None or row[0] is None:
        return None
    return (datetime.datetime.now(datetime.UTC) - row[0]).total_seconds() / 3600.0


def replace_store_products(
    conn: DBConn, store_key: str, records: list[StoreProductRecord]
) -> int:
    """Replace one store's menu snapshot wholesale. Caller commits.

    Menus churn daily, so a complete re-scrape is the new truth — but two cases are
    guarded so a bad scrape can't destroy a good snapshot:

    - an EMPTY result keeps the prior snapshot (a transient fetch failure must not
      wipe a good menu);
    - a result that collapses to under ``_MENU_RETAIN_FRACTION`` of a still-fresh
      prior (younger than ``_MENU_RETAIN_MAX_AGE_HOURS``) is treated as a throttled
      PARTIAL fetch and rejected — a 406-truncated fragment must not overwrite a full
      snapshot. Once the prior ages out of the window a genuine shrink still lands.

    Returns the resulting product count (the kept prior count when a guard fires).
    """
    if not records:
        return count_store_products(conn, store_key)
    prior = count_store_products(conn, store_key)
    if prior and len(records) < prior * _MENU_RETAIN_FRACTION:
        age_hours = _snapshot_age_hours(conn, store_key)
        if age_hours is not None and age_hours < _MENU_RETAIN_MAX_AGE_HOURS:
            print(
                f"  store_menu: {store_key} kept prior {prior} products "
                f"(re-scrape yielded {len(records)} < {_MENU_RETAIN_FRACTION:.0%} of "
                f"a {age_hours:.0f}h-old snapshot — likely a throttled partial)"
            )
            return prior
    conn.execute("DELETE FROM store_products WHERE store_key = %s", (store_key,))
    for record in records:
        insert_store_product(conn, record)
    return len(records)


# Categories whose products feed the master-DB observation history. Widened from flower-only to every
# CONSUMABLE category (those carrying price/potency/terpene/cannabinoid signal worth a time series);
# Accessory / Other / uncategorized are excluded (gear + grab-bag, no chemistry). For mg-dosed
# categories (edibles/tinctures/beverages/capsules) the dose is folded into product_fingerprint
# (thc_mg/cbd_mg), so a 10mg vs 100mg gummy of the same name are distinct identities.
_OBSERVED_CATEGORIES = (
    "Flower", "Pre-Roll", "Vape", "Concentrate", "Edible",
    "Beverage", "Tincture", "Topical", "Capsule",
)


def _observation_changed(prev: tuple, record: StoreProductRecord) -> bool:
    """Whether a record's price/potency/terpenes/cannabinoids differ from the last observation ``prev``
    = (product_id, price, thc, cbd, thc_mg, cbd_mg, terp_total, terpenes_std, cannabinoids_std, date)."""
    return (
        prev[1] != record.price or prev[2] != record.thc or prev[3] != record.cbd
        or prev[4] != record.thc_mg or prev[5] != record.cbd_mg or prev[6] != record.terp_total
        or (prev[7] or None) != (record.terpenes_std or None)
        or (prev[8] or None) != (record.cannabinoids_std or None)
    )


def record_observations(
    conn: DBConn, store_key: str, records: list[StoreProductRecord],
    *, now: datetime.datetime | None = None,
) -> int:
    """Append price/potency/terpene history for a store's observed-category products. Caller commits.

    The master-DB write path, run alongside the snapshot replace:
    upsert each product's identity into ``products``, then APPEND a ``product_observations`` row when
    its price/potency/terpenes changed since the last observation at this store, or once a day as a
    presence heartbeat. Append-only — rows are never updated or deleted. Scoped to consumable
    categories (``_OBSERVED_CATEGORIES``). ``now`` (the observation timestamp) defaults to the current UTC time;
    it is injectable for tests. Returns the number of observation rows appended.
    """
    observed_at = now or datetime.datetime.now(datetime.UTC)
    by_fp: dict[str, StoreProductRecord] = {}
    for record in records:
        if record.category_std not in _OBSERVED_CATEGORIES:
            continue
        fingerprint = text.product_fingerprint(
            record.brand, record.name, record.size_g, record.product_type_std,
            thc_mg=record.thc_mg, cbd_mg=record.cbd_mg,
        )
        if fingerprint:
            by_fp[fingerprint] = record  # a menu may list one product twice — keep the last
    if not by_fp:
        return 0

    # Upsert product identities → fingerprint:id (first_seen kept; last_seen bumped on re-sight).
    fp_to_id: dict[str, int] = {}
    for fingerprint, record in by_fp.items():
        row = conn.execute(
            "INSERT INTO products (fingerprint, brand, name, category_std, product_type_std, "
            "obtention_std, strain_type_std, size_g) VALUES (%s, %s, %s, %s, %s, %s, %s, %s) "
            "ON CONFLICT (fingerprint) DO UPDATE SET last_seen = now() RETURNING id",
            (fingerprint, text.normalize_brand(record.brand or ""), record.name,
             record.category_std, record.product_type_std, record.obtention_std,
             record.strain_type_std, record.size_g),
        ).fetchone()
        assert row is not None  # INSERT ... RETURNING always yields a row
        fp_to_id[fingerprint] = row[0]

    # The last observation per product at this store, for change detection.
    last: dict[int, tuple] = {}
    for row in conn.execute(
        "SELECT DISTINCT ON (product_id) product_id, price, thc, cbd, thc_mg, cbd_mg, terp_total, "
        "terpenes_std, cannabinoids_std, scraped_at::date FROM product_observations "
        "WHERE store_key = %s AND product_id = ANY(%s) ORDER BY product_id, scraped_at DESC",
        (store_key, list(fp_to_id.values())),
    ):
        last[row[0]] = row

    appended = 0
    for fingerprint, record in by_fp.items():
        product_id = fp_to_id[fingerprint]
        prev = last.get(product_id)
        same_day = prev is not None and prev[9] == observed_at.date()
        if prev is not None and same_day and not _observation_changed(prev, record):
            continue  # already have today's row and nothing changed — no duplicate heartbeat
        conn.execute(
            "INSERT INTO product_observations (product_id, store_key, state, price, thc, cbd, "
            "thc_mg, cbd_mg, terpenes_std, terp_total, cannabinoids_std, scraped_at) "
            "VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)",
            (product_id, store_key, record.state, record.price, record.thc, record.cbd,
             record.thc_mg, record.cbd_mg,
             Jsonb(record.terpenes_std) if record.terpenes_std is not None else None,
             record.terp_total,
             Jsonb(record.cannabinoids_std) if record.cannabinoids_std is not None else None,
             observed_at),
        )
        appended += 1
    return appended


