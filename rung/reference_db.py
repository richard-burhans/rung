def upsert_store_location(
    conn: DBConn, *, location_key: str, state: str, latitude: float | None,
    longitude: float | None, address: str | None, city: str | None, zip_code: str | None,
) -> int:
    """Upsert a physical-location identity, returning its id. Keeps ``first_seen``, bumps
    ``last_seen`` and refreshes the representative attrs on re-sight. Caller commits."""
    row = conn.execute(
        "INSERT INTO store_locations (location_key, state, latitude, longitude, address, city, "
        "zip_code) VALUES (%s, %s, %s, %s, %s, %s, %s) "
        "ON CONFLICT (location_key) DO UPDATE SET last_seen = now(), state = EXCLUDED.state, "
        "latitude = EXCLUDED.latitude, longitude = EXCLUDED.longitude, address = EXCLUDED.address, "
        "city = EXCLUDED.city, zip_code = EXCLUDED.zip_code RETURNING id",
        (location_key, state, latitude, longitude, address, city, zip_code),
    ).fetchone()
    assert row is not None  # INSERT ... RETURNING always yields a row
    return row[0]


def latest_store_observations(
    conn: DBConn, location_ids: list[int], source: str
) -> dict[int, tuple]:
    """The most recent observation per location for a source, for change detection. Returns
    ``location_id -> (operator, storefront_name, platform, external_id, address, city,
    observed_at::date)``."""
    if not location_ids:
        return {}
    rows = conn.execute(
        "SELECT DISTINCT ON (location_id) location_id, operator, storefront_name, platform, "
        "external_id, address, city, observed_at::date FROM store_observations "
        "WHERE source = %s AND location_id = ANY(%s) ORDER BY location_id, observed_at DESC",
        (source, location_ids),
    ).fetchall()
    return {row[0]: row[1:] for row in rows}


def append_store_observation(
    conn: DBConn, *, location_id: int, source: str, operator: str | None,
    storefront_name: str | None, platform: str | None, external_id: str | None,
    address: str | None, city: str | None, observed_at: datetime.datetime,
) -> None:
    """Append one append-only store observation. Caller commits."""
    conn.execute(
        "INSERT INTO store_observations (location_id, source, operator, storefront_name, platform, "
        "external_id, address, city, observed_at) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)",
        (location_id, source, operator, storefront_name, platform, external_id, address, city,
         observed_at),
    )


def record_location_observations(
    conn: DBConn, source: str, observations: dict[str, LocationObservation],
    *, now: datetime.datetime | None = None,
) -> int:
    """The shared store-lifecycle write engine: upsert identities, append what changed. Caller commits.

    ``observations`` is one :class:`LocationObservation` per ``location_key`` — the caller computes
    the keys (``dedupe`` lives above ``db`` in the layering) and collapses same-rooftop duplicates
    with its own preference rule. For each: upsert the identity into ``store_locations``
    (keep ``first_seen``), then APPEND a ``store_observations`` row when the observed
    operator/storefront/handle/address changed since the last observation for this
    ``(location, source)``, or once a day as a presence heartbeat. Append-only; both the Stage-2
    ``company_site`` and Stage-1 ``state_roster`` captures drive this one engine
    (docs/store_history_design.md). Returns the number of observation rows appended.
    """
    if not observations:
        return 0
    observed_at = now or datetime.datetime.now(datetime.UTC)
    by_location: dict[int, LocationObservation] = {}
    for location_key, obs in observations.items():
        location_id = upsert_store_location(
            conn, location_key=location_key, state=obs.state, latitude=obs.latitude,
            longitude=obs.longitude, address=obs.address, city=obs.city, zip_code=obs.zip_code,
        )
        by_location[location_id] = obs

    last = latest_store_observations(conn, list(by_location), source)
    appended = 0
    for location_id, obs in by_location.items():
        observed = (obs.operator, obs.storefront_name, obs.platform, obs.external_id,
                    obs.address, obs.city)
        prev = last.get(location_id)  # (operator, storefront, platform, external_id, address, city, date)
        same_day = prev is not None and prev[6] == observed_at.date()
        if prev is not None and same_day and prev[:6] == observed:
            continue  # already have today's row and nothing changed — no duplicate heartbeat
        append_store_observation(
            conn, location_id=location_id, source=source, operator=obs.operator,
            storefront_name=obs.storefront_name, platform=obs.platform,
            external_id=obs.external_id, address=obs.address, city=obs.city,
            observed_at=observed_at,
        )
        appended += 1
    return appended


def replace_lifecycle_events(
    conn: DBConn, state: str, events: list[LifecycleEventRecord],
) -> int:
    """Replace one state's derived ``store_lifecycle_events`` with ``events``. Caller commits.

    A wholesale replace, NOT the keep-the-best guard ``replace_company_stores`` uses. That guard
    protects a *scrape* — a live snapshot a transient failure could clobber. These rows are a
    *derivation* over the append-only log, so recomputing them is free, deterministic, and the only
    way to let later evidence revise an earlier verdict: a store called "closed" that reappears next
    cycle must lose that event. Fewer events than last time is therefore a legitimate outcome, and
    the log — not this table — is the thing that must never be lost.

    Returns the number of event rows written.
    """
    conn.execute("DELETE FROM store_lifecycle_events WHERE state = %s", (state,))
    for event in events:
        conn.execute(
            "INSERT INTO store_lifecycle_events (location_id, state, source, kind, occurred_on, "
            "operator, previous_operator, confidence, related_location_id) "
            "VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)",
            (event.location_id, event.state, event.source, event.kind, event.occurred_on,
             event.operator, event.previous_operator, event.confidence, event.related_location_id),
        )
    return len(events)


