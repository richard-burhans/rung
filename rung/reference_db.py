# Weedmaps/Leafly are last-resort DIRECTORY listings: a handle exists, but the operator's
# real menu usually isn't there (their Stage-3 rungs frequently return empty). A store
# discovered via a real platform rung (Jane/Dutchie/Sweed/Cresco/Hytiva/Trulieve/Curaleaf —
# whose `platform` may even read "custom") is "menu-bearing" and worth more than an
# aggregator row. So keep-the-best prefers the result with MORE menu-bearing stores even at
# a total-count loss, and never downgrades menu-bearing stores to aggregator-only ones.
_AGGREGATOR_SOURCES = frozenset({"weedmaps_directory", "leafly_directory"})
_AGGREGATOR_PLATFORMS = frozenset({"weedmaps", "leafly"})


def _distinct_stored_menu_stores(conn: DBConn, company_id: int, abbr: str) -> int:
    """Stored DISTINCT physical stores carrying a NON-aggregator (real-menu-rung) handle."""
    row = conn.execute(
        "SELECT COUNT(DISTINCT lower(coalesce(nullif(trim(address), ''), name, ''))) "
        "FROM company_stores WHERE company_id = %s AND state = %s "
        "AND external_id IS NOT NULL AND external_id != '' "
        "AND coalesce(source, '') NOT IN ('weedmaps_directory', 'leafly_directory') "
        "AND coalesce(platform, '') NOT IN ('weedmaps', 'leafly')",
        (company_id, abbr),
    ).fetchone()
    return row[0] if row else 0


def _distinct_new_menu_stores(records: list[CompanyStoreRecord]) -> int:
    """Distinct physical stores in a scrape result carrying a non-aggregator handle."""
    return len({
        ((record.address or "").strip() or record.name or "").lower()
        for record in records
        if record.external_id
        and (record.source or "") not in _AGGREGATOR_SOURCES
        and (record.platform or "") not in _AGGREGATOR_PLATFORMS
    })


# A handle-upgrade may overwrite slightly fewer stores only if it retains at least this
# fraction of them — the Stage-3 `external_id` is worth a small count loss, a big one isn't.
_HANDLE_UPGRADE_RETENTION = 0.8

# A real-menu-handle upgrade (aggregator/plain → Jane/Dutchie/…) may lose more stores than a
# bare handle-upgrade — empty aggregator rows are low-value — but must still retain at least
# this fraction (so a real 15→1 collapse is rejected while a 4→3 menu upgrade is accepted).
_MENU_UPGRADE_RETENTION = 0.5


def replace_company_stores(
    conn: DBConn,
    company_id: int,
    abbr: str,
    records: list[CompanyStoreRecord],
) -> tuple[int, bool]:
    """Guarded, quality-aware keep-the-best replace of one company's stores. Caller commits.

    Overwrites the company's rows when the new scrape yields **at least as many** stores as
    are stored (so a transient zero/low-yield re-run can't clobber good data) — and, on a
    tie, the fresher result wins. Both sides are counted as DISTINCT physical stores
    (address, name as fallback), not raw rows, so a noisy extractor that emitted two rows
    per store can't entrench itself against a clean result. As a quality refinement, a new
    result that adds Stage-3 ``external_id`` handles where the stored rows have none also
    wins even if it has slightly fewer stores (it must retain ≥
    ``_HANDLE_UPGRADE_RETENTION`` of them), since a store row with a menu handle is worth
    more than a bare address.

    Quality dominates count among menu-platform handles (Jane/Dutchie/Sweed/… — NOT the
    Weedmaps/Leafly directory listings, whose menus are usually empty): a result with **more**
    of them wins as long as it retains ≥ ``_MENU_UPGRADE_RETENTION`` of the stores (so 4 empty
    Leafly listings yield to 3 Jane handles, but a 15→1 collapse is still rejected), and a
    result with **fewer** never clobbers (no downgrading real menus to a bigger empty-aggregator
    sweep). Only when both sides have the same menu-platform count does the distinct-count /
    freshness / handle-upgrade logic below decide. Returns ``(resulting_count, kept_prior)`` —
    ``kept_prior`` is True when better prior data was retained.
    """
    existing = count_company_stores(conn, company_id, abbr)
    if not records:
        return existing, False

    existing_distinct = _distinct_stored_stores(conn, company_id, abbr)
    new_distinct = _distinct_new_stores(records)
    existing_menu = _distinct_stored_menu_stores(conn, company_id, abbr)
    new_menu = _distinct_new_menu_stores(records)
    if new_menu < existing_menu:
        # Never downgrade real-menu handles to fewer of them (or to aggregator-only rows),
        # even if the new set has more total stores — protects a Jane/Dutchie set from a
        # transient low-yield re-scrape or a larger empty-aggregator sweep.
        overwrite = False
    elif new_menu > existing_menu:
        # Gained real-menu handles (e.g. 4 empty Leafly listings → 3 Jane handles): accept
        # unless it would lose more than half the stores (a 15→1 collapse is still rejected).
        overwrite = new_distinct >= existing_distinct * _MENU_UPGRADE_RETENTION
    else:
        overwrite = new_distinct >= existing_distinct
        if not overwrite:
            handle_row = conn.execute(
                "SELECT COUNT(*) FROM company_stores WHERE company_id = %s AND state = %s "
                "AND external_id IS NOT NULL AND external_id != ''",
                (company_id, abbr),
            ).fetchone()
            existing_handles = handle_row[0] if handle_row else 0
            new_handles = sum(1 for record in records if record.external_id)
            overwrite = (
                existing_handles == 0
                and new_handles > 0
                and new_distinct >= existing_distinct * _HANDLE_UPGRADE_RETENTION
            )
    if not overwrite:
        return existing, True

    delete_company_stores_for_company(conn, company_id, abbr)
    for record in records:
        insert_company_store(conn, record)
    return len(records), False


