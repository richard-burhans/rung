# The percent-OR-mg potency CHECK, added in-place to a database created before the constraint
# existed. Postgres has no ADD CONSTRAINT IF NOT EXISTS, so the DO block swallows duplicate_object
# to stay idempotent (a fresh table already carries the constraint from _CREATE_STORE_PRODUCTS).
# Pre-flight scan 2026-07-02: 0 of 4.8M live rows violate it, so it validates without cleanup.
_MIGRATE_STORE_PRODUCTS_POTENCY_CHECK = """
DO $$ BEGIN
    ALTER TABLE store_products ADD CONSTRAINT store_products_potency_unit_check
        CHECK ((thc IS NULL OR thc_mg IS NULL) AND (cbd IS NULL OR cbd_mg IS NULL));
EXCEPTION WHEN duplicate_object THEN NULL; END $$;
"""

