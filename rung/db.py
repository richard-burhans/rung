def get_connection() -> DBConn:
    """Open and return a connection to the dispensaries database.

    Reads DATABASE_URL from the environment (default: local dev Postgres container).
    """
    url = os.environ.get("DATABASE_URL", _DEFAULT_DATABASE_URL)
    try:
        return psycopg.connect(url)
    except psycopg.OperationalError as exc:
        raise psycopg.OperationalError(
            f"{exc} — is the dev Postgres running?"
        ) from exc


def one(conn: DBConn, query: LiteralString, params: tuple = ()) -> tuple:
    """Run a query expected to return exactly one row and return it (never None).

    For aggregate / ``RETURNING`` / single-row SELECTs where ``fetchone()`` is logically non-None.
    Centralizes the ``Row | None`` narrowing so callers (esp. analysis scripts) can subscript the
    result directly without a per-call ``assert``. Raises if the query yields no row.
    """
    row = conn.execute(query, params).fetchone()
    if row is None:
        raise LookupError("query returned no row")
    return row


def create_tables(conn: DBConn) -> None:
    """Create all tables if they do not already exist.

    Does not create the companies table — that is owned by seed_companies.py.
    """
    conn.execute(_CREATE_DISPENSARIES)
    conn.execute(_CREATE_COMPANY_RECON)
    conn.execute(_CREATE_COMPANY_STORES)
    conn.execute(_CREATE_ACCESS_METHODS)
    conn.execute(_CREATE_STATE_PROGRAMS)
    conn.execute(_CREATE_JOBS)
    conn.execute(_CREATE_JOBS_LIVE_UNIQUE)
    conn.execute(_CREATE_JOBS_PENDING_CLAIM)
    _migrate_jobs(conn)
    conn.execute(_CREATE_TOKEN_BUCKETS)
    conn.execute(_CREATE_PROXIES)
    conn.execute(_CREATE_PROXIES_CLAIM_INDEX)
    conn.execute(_CREATE_PROXY_TIERS)
    conn.execute(_CREATE_STORE_PRODUCTS)
    conn.execute(_CREATE_STORE_PRODUCTS_STORE_INDEX)
    conn.execute(_CREATE_STORE_PRODUCTS_STATE_INDEX)
    conn.execute(_CREATE_COMPANY_STORES_STATE_INDEX)
    conn.execute(_CREATE_PRODUCTS)
    conn.execute(_CREATE_PRODUCT_OBSERVATIONS)
    conn.execute(_CREATE_PRODUCT_OBSERVATIONS_PRODUCT_INDEX)
    conn.execute(_CREATE_PRODUCT_OBSERVATIONS_STORE_INDEX)
    conn.execute(_CREATE_STORE_LOCATIONS)
    conn.execute(_CREATE_STORE_OBSERVATIONS)
    conn.execute(_CREATE_STORE_OBSERVATIONS_LOCATION_INDEX)
    _migrate_company_stores(conn)
    _migrate_state_programs(conn)
    _migrate_store_products(conn)
    _migrate_product_observations(conn)
    _migrate_dispensaries(conn)
    conn.execute(_CREATE_PRODUCTS_NORMALIZED_VIEW)  # after the migration adds its columns
    conn.commit()


