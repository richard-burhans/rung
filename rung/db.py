def get_state_program(conn: DBConn, abbr: str) -> StateProgramRecord | None:
    """Return the stored StateProgramRecord for a state, or None."""
    row = conn.execute(
        "SELECT abbr, name, programs, program_term, agency, best_url, source_type, "
        "all_gov_urls, last_checked, check_status, searched_at, error, "
        "list_url, list_type, list_found_at, list_status "
        "FROM state_programs WHERE abbr = %s",
        (abbr,),
    ).fetchone()
    if row is None:
        return None
    return _row_to_state_program(row)


def get_all_state_programs(conn: DBConn) -> list[StateProgramRecord]:
    """Return all stored StateProgramRecords ordered by state name."""
    rows = conn.execute(
        "SELECT abbr, name, programs, program_term, agency, best_url, source_type, "
        "all_gov_urls, last_checked, check_status, searched_at, error, "
        "list_url, list_type, list_found_at, list_status "
        "FROM state_programs ORDER BY name"
    ).fetchall()
    return [_row_to_state_program(row) for row in rows]


def _row_to_state_program(row: tuple) -> StateProgramRecord:
    abbr, name, programs, program_term, agency, best_url, source_type, \
        all_gov_urls_json, last_checked, check_status, searched_at, error, \
        list_url, list_type, list_found_at, list_status = row
    all_gov_urls: list[str] = []
    if all_gov_urls_json:
        with contextlib.suppress(ValueError, TypeError):
            all_gov_urls = json.loads(all_gov_urls_json)
    return StateProgramRecord(
        abbr=abbr,
        name=name,
        programs=programs,
        program_term=program_term or "",
        agency=agency or "",
        best_url=best_url,
        source_type=source_type,
        all_gov_urls=all_gov_urls,
        last_checked=last_checked,
        check_status=check_status or "never",
        searched_at=searched_at,
        error=error,
        list_url=list_url,
        list_type=list_type,
        list_found_at=list_found_at,
        list_status=list_status,
    )


