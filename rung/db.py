def realign_store_products_company(conn: DBConn, abbr: str) -> int:
    """Re-point a state's menu snapshots at the CURRENT kept company row for each handle.

    A snapshot is stamped with the company_id that owned its ``(platform, external_id)`` handle
    when it was scraped. A later dedupe can move the kept (canonical) row to a different company
    of the same operator cluster — Sunnyside's PA stores were scraped under the "Delta 9
    Pittsburgh" alias, then dedupe made "Sunnyside" canonical — but the snapshots keep the stale
    id, so per-operator menu reporting mis-attributes them. This re-stamps every snapshot to the
    company of the current kept row (``canonical_company_id IS NULL``, DISTINCT ON the handle),
    which is exactly the row :func:`get_menu_stores_for_state` scrapes under, so a snapshot is
    always filed under the same company a fresh scrape would use. Idempotent; returns rows moved.

    Handle-scoped: re-attribution keys on ``(platform, external_id)``, so a snapshot whose OWN
    handle was the folded (non-kept) side of a cross-handle merge maps to no kept row and keeps its
    prior company_id — a rare residual (one rooftop menu-scraped under two distinct handles, then
    folded; the folded row still exists, so the snapshot is not an orphan the menu-prune would catch).
    """
    cur = conn.execute(
        "UPDATE store_products sp SET company_id = k.company_id "
        "FROM (SELECT DISTINCT ON (platform, external_id) platform, external_id, company_id "
        "      FROM company_stores "
        f"      WHERE state = %s AND {_KEPT_HANDLE_WHERE} "
        f"      {_KEPT_HANDLE_ORDER}) k "
        "WHERE sp.state = %s AND sp.platform = k.platform AND sp.external_id = k.external_id "
        "  AND sp.company_id <> k.company_id",
        (abbr, abbr),
    )
    return cur.rowcount


